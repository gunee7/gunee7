package java04;

public class jv04_02_중첩for {
    public static void main(String[] args){
        
        for(int x =1; x<=5; x=x+1) {
            for(int y =1; y<=10; y=y+1) {
                
                System.out.print("*");
            }
            System.out.println();
        }
      
        
    }
}
